package ejercicio8;

public class EjercicioOchoApp {

	public static void main(String[] args) {

		System.out.println("Números del 1 al 100 con un FOR");
		
		/* Declaramos la variable i inicializada en 1. Mientras i sea menor o igual a 100, mostraremos
		por pantalla la variable i y después incrementaremos su valor en 1. i++; */
		for (int i = 1; i <= 100; i++) {
			System.out.println(i);
		}

	}

}
